public class Periodicite {
 private int fois;
 private int jours;
public int getFois() {
    return fois;
}
public void setFois(int fois) {
    this.fois = fois;
}
public int getJours() {
    return jours;
}
public void setJours(int jours) {
    this.jours = jours;
}
public Periodicite(int fois,int jours){
    this.fois=fois;
    this.jours=jours;
}
}
